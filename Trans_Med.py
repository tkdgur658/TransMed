class Trans_Med_Embedding(nn.Module): # (batch_size, num_modalities, width, height) -> (batch_size, num_modalities/3, embedding)
    # (16, 78, 224, 224) -> (16, 26, 512)
    def __init__(self, num_modalities, embed_dim=384, drop_rate=0):
        super().__init__()
        cnn_layer_list = list(ResNet18(3, 1).children())[:-1]
        self.cnn = nn.Sequential(*cnn_layer_list)
        self.linear_projection = nn.Linear(512, embed_dim)
        self.num_tokens = 1
        self.num_patches = num_modalities//3
        self.cls_token = nn.Parameter(torch.randn(1, 1, embed_dim))
        self.pos_embed = nn.Parameter(torch.randn(1, self.num_patches + self.num_tokens, embed_dim))
        self.pos_drop = nn.Dropout(p=drop_rate)

    def forward(self, x):
        batch_size, num_modalities, h, w = x.shape
        x = rearrange(x, 'b (m c) h w -> b m c h w', b=batch_size, c=3, h=h, w=w)
        for i in range(x.shape[1]):
            if i==0:
                features = rearrange(self.cnn(x[:,i]), 'b c h w -> b 1 (c h w)')
            else:
                features = torch.cat([features, rearrange(self.cnn(x[:,i]), 'b c w h -> b 1 (c h w)')], 1)
        features = self.linear_projection(features)
        cls_token = self.cls_token.expand(features.shape[0], -1, -1)
        features= torch.cat((cls_token, features), dim=1)
        features = self.pos_drop(features + self.pos_embed)
        return features


